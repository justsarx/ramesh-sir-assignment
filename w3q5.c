//Write a program to display numbers 1 to 100 using ASCII value from 48 to 57. Use the nested loop.

#include <stdio.h>
int main()
{

    int i, j, k;
    for(i=1; i<=1; i++)
    {
        for(j=1; j<=10; j++)
        {
            for(k=1; k<=10; k++)
            {
                printf("%c%c%c ", i+47, j+47, k+47);
            }
            printf("\n");
        }
        printf("\n");
    }
    return 0;
}
